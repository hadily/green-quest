    /**
     * Find clients by admin.
     *
     * @param Admin $admin
     * @return Client[]
     */
    public function findByAdmin(Admin $admin): array
    {
        return $this->createQueryBuilder('c')
            ->andWhere('c.admin = :admin')
            ->setParameter('admin', $admin)
            ->getQuery()
            ->getResult();
